    private void jbBuscarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarPacienteActionPerformed
        // TODO add your handling code here:
        String cpf = jtCPFBuscar.getText();
        if(!preencheu()) { // Se nada for digitado
            JOptionPane.showMessageDialog(this, "Preencher CPF para buscar paciente", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                File arquivo = new File(cpf+".txt"); // Abrir arquivo
                if(!arquivo.exists()) { // Verificando se arquivo existe 
                    JOptionPane.showMessageDialog(this, "O paciente com CPF: "+cpf+" não cadastrado.", "Erro de busca", JOptionPane.ERROR_MESSAGE);
                    jtCPFBuscar.setText("");
                } else {
                    // Caso exista
                    FileReader ler = new FileReader(arquivo);
                    BufferedReader organizador = new BufferedReader(ler);

                    ArrayList<String> information = new ArrayList<>();

                    while(organizador.ready()) {
                        information.add(organizador.readLine()); // Adicionado informação arquivo para a lista
                    }
                    
                    // Inserir informação nos jTextField
                    jtNome.setText(information.get(0));
                    jtTelefone.setText(information.get(1));
                    jtCelular.setText(information.get(2));
                    jtCPF.setText(information.get(3));
                    jtRG.setText(information.get(4));
                    jtEmail.setText(information.get(5));
                    jtNascimento.setText(information.get(6));
                    jtSexo.setText(information.get(7));
                    jtEstadoCivil.setText(information.get(8)); 
                    jtProfissao.setText(information.get(9));
                    jtNomeMae.setText(information.get(10));
                    jtNomePai.setText(information.get(11));
                    jtCor.setText(information.get(12));
                    jtLogradouro.setText(information.get(13));
                    jtComplemento.setText(information.get(14));
                    jtNumero.setText(information.get(15));
                    jtBairro.setText(information.get(16));
                    jtCidade.setText(information.get(17));
                    jtCEP.setText(information.get(18));
                    jtEstado.setText(information.get(19));
                    jtDataSolicitacao.setText(information.get(20));
                    jtMedicoSolicitante.setText(information.get(21));
                    jtMedicoExecutante.setText(information.get(22));
                    jtTipoExame.setText(information.get(23));
                    jtSaude.setText(information.get(24));
                    jtSangue.setText(information.get(25));
                    jtObservacao.setText(information.get(26));
                    
                    jTabbedPane1.setVisible(true); // jTabbedPane as visible to show all information
                    jPanel4.setVisible(false); 
                       
                    jtCPFBuscar.setText("");
                    
                    organizador.close();
                } 
            } catch(Exception e) {
                System.out.printf("Erro");
            }
        }
    }//GEN-LAST:event_jbBuscarPacienteActionPerformed

