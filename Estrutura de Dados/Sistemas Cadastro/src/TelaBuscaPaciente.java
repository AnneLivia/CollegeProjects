    boolean preencheu() { // Verificar se foi preenchido o CPF da busca
        String cpf = jtCPFBuscar.getText();
        int contSpace = 0;
        for(int i = 0; i < cpf.length(); i++) {
            if(cpf.charAt(i) == ' ') {
                contSpace++;
            }
        }
        if(contSpace == 0)
            return true;
        return false;
    }
    
