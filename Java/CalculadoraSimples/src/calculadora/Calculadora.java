    private void Numeros(String n) {
        if(textField.getText().equals("0") || textField.getText().equals("0.0"))
        {
            textField.setText(null);
        }
        backNumber = temporaria;
        temporaria = textField.getText();
        temporaria = temporaria + n;
        textField.setText(temporaria);
    }
   
