    private void btResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btResultActionPerformed
       switch(operacao)
       {
           case "+" : resultado = guardado + n1;
                      textField.setText(String.valueOf(resultado));
                      n1 = resultado;
                      teste = true;
                      backNumber = String.valueOf(resultado);
                      break;
           case "-" : resultado = guardado - n1;
                      textField.setText(String.valueOf(resultado));
                      n1 = resultado;
                      teste = true;
                      backNumber = String.valueOf(resultado);
                      break;
           case "x" : resultado = guardado * n1;
                      textField.setText(String.valueOf(resultado));
                      n1 = resultado;
                      teste = true;
                      backNumber = String.valueOf(resultado);
                      break;
           case "/" : resultado = guardado / n1;
                      textField.setText(String.valueOf(resultado));
                      n1 = resultado;
                      teste = true;
                      backNumber = String.valueOf(resultado);
                      break;
       }
    }//GEN-LAST:event_btResultActionPerformed

